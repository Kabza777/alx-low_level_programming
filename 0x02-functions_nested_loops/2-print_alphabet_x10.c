#include "main.h"
/**
 * print_alphabet - Entry point
 *
 * return: Always 0 (success)
 */

void print_alphabet
{                                                                                                                               

	        char c = 'a';                                                                                                           

		                                                                                                                                

		        while                                                                                                                   

				        (c <= 'z');                                                                                                             

			        {                                                                                                                       

					                _putchar(c);                                                                                                    

							                c++;                                                                                                            

									        }                                                                                                                       

				        _putchar('\n');                                                                                                         

} 
